# Python program to print positive Numbers in a List 
  
# list of numbers 
list1 = [12, -7, 5, 64, -14] 
list2 = [12, 14, -95, 3]
  
# iterating each number in list 
for num1 in list1: 
    
# checking condition 
    if num1 >= 0: 
       print(num1, end = " ")
       
    
for num2 in list2:
 
    if num2 >= 0:
       print(num2, end = " ")
       
